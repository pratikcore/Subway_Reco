export const DashboardStore = (state = StorageArea, action) => {
  switch (action.type) {
    case "UPDATE_DASHBOARD_TABLE_LIST":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            inStoreTableList: action.payload.value,
          },
        },
      };
      break;

    case "UPDATE_SELECTED_ROW_IN_TABLE":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            inStoreSelectedRowInTable: action.payload.value || [],
          },
        },
      };
      break;

    case "UPDATE_3PO_TABLE_LIST":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            _3poTableList: action.payload.value,
          },
        },
      };
      break;
      
    case "UPDATE_RECO_3PO_TABLE_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            reco3poTable: action.payload.value,
          },
        },
      };
      break;

    case "UPDATE_3PO_ROW_IN_TABLE":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            _3poSelectedRowInTable: action.payload.value || [],
          },
        },
      };
      break;

    case "UPDATE_DASHBOARD_TABLE_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            DashboardTableData: action.payload.value || {},
          },
        },
      };
      break;

    case "UPDATE_3PO_TABLE_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            _3poTableData: action.payload.value || {},
          },
        },
      };
      break;

    case "UPDATE_USER_CREDENTIALS_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            userCredentials: action.payload.value || {},
          },
        },
      };
      break;

    case "UPDATE_DATE_RANGE_SELECT_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            dateRangeSelect: action.payload.value || {},
          },
        },
      };
      break;

    case "MAPPED_THREEPO_LIST_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            mappingStoreData: action.payload.value || [],
          },
        },
      };
      break;

    case "RECO_CARD_UPI_DATA":
      return {
        ...state,
        ...{
          DashboardStore: {
            ...state.DashboardStore,
            recoCardUPiData: action.payload.value || [],
          },
        },
      };
      break;

    default:
      return { ...state };
      break;
  }
};
